package com.lp.server.artikel.service;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class WebshopItemReferenceInfo implements Serializable {
	private static final long serialVersionUID = -696093181380378250L;

	private WebshopItemMimeInfo mimeInfo ;
	private byte[] image ;
	private String shortDescription ;
	private String longDescription ;
	private String remark ;
	
	public WebshopItemMimeInfo getMimeInfo() {
		return mimeInfo;
	}
	public void setMimeInfo(WebshopItemMimeInfo mimeInfo) {
		this.mimeInfo = mimeInfo;
	}
	public byte[] getImage() {
		return image;
	}
	public void setImage(byte[] image) {
		this.image = image;
	}
	public String getShortDescription() {
		return shortDescription;
	}
	public void setShortDescription(String shortDescription) {
		this.shortDescription = shortDescription;
	}
	public String getLongDescription() {
		return longDescription;
	}
	public void setLongDescription(String longDescription) {
		this.longDescription = longDescription;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}	
}
